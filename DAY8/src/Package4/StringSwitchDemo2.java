package Package4;

public class StringSwitchDemo2 {

	public static void main(String[] args) {
					
		String day = "Sunday";
		
		switch(day) 
		{
		case "Monday":
			System.out.println("Go to Attend college");
			break;
		
		case "Thursday":
			System.out.println("Go to Coaching classes");
			break;
			
		case "Saturday":
			System.out.println("Go for Picnic");
			break;	
			
		case "Sunday":
			System.out.println("Relax !!! Weekly off");
			break;		
	
		default:
			break;
		}	
	}
}
